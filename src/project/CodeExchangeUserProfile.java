package project;

/**
 *
 * @author Rivera
 */
public class CodeExchangeUserProfile {
    
    private String name;
    private String company_name;
    private String email;
    private String number;
    private String language;
    private String password;
    private int spendings;

    CodeExchangeUserProfile(String name) {
        setName(name);
    }
    /**
     * @return the spendings
     */
    public int getSpendings() {
        return spendings;
    }

    /**
     * @param spendings the spendings to set
     */
    public void setSpendings(int spendings) {
        this.spendings = spendings;
    }

    

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the company_name
     */
    public String getCompany_name() {
        return company_name;
    }

    /**
     * @param company_name the company_name to set
     */
    public void setCompany_name(String company_name) {
        this.company_name = company_name;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the number
     */
    public String getNumber() {
        return number;
    }

    /**
     * @param number the number to set
     */
    public void setNumber(String number) {
        this.number = number;
    }

    /**
     * @return the language
     */
    public String getLanguage() {
        return language;
    }

    /**
     * @param language the language to set
     */
    public void setLanguage(String language) {
        this.language = language;
    }

    /**
     * @return the password
     */
    public String getPassword() {
        return password;
    }

    /**
     * @param password the password to set
     */
    public void setPassword(String password) {
        this.password = password;
    }
    
}
